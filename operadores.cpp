#include <iostream>

using namespace std;

int main() 
{
    // processamento
    int mult = 10 * 5;
    int soma = 500 + 400;
    float divi = 100.0 / 3.0;
    int sub = 10 - 11;
    float resto = 11 % 3;

    // Saída
    cout << "Multiplicação: " << mult << endl;
    cout << "Soma: " << soma << endl;
    cout << "Divisão: " << divi << endl;
    cout << "Subtração: " << sub << endl;
    cout << "Resto da divisão: " << resto << endl;
    
    return 0;
}